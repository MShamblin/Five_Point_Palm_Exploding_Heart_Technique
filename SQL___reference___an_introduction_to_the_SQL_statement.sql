'An introduction to the SQL statement'

'
SQL statements used to work with data (DML) 
SELECT              Retrieves data from one or more tables. 
INSERT              Adds one or more new rows to a table.
UPDATE              Changes one or more existing rows in a table. 
DELETE              Deletes one or more existing rows from a table.


SQL statements used to work with database objects (DDL)
CREATE DATABASE         Creates a new database. 
CREATE TABLE            Creates a new table in a database. 
CREATE INDEX            Creates a new index for a table.
ALTER TABLE             Changes the structure of an existing table. 
ALTER INDEX             Changes the structure of an existing index.
DROP DATABASE           Deletes an existing database. 
DROP TABLE              Deletes an existing table. 
DROP INDEX              Deletes an existing index.



Description 
    • The SQL statements can be divided into two categories: the data manipulation language 
        (DML) that lets you work with the data in the database and the data definition 
        language (DDL) that lets you work with the objects in the database.
    • SQL programmers typically work with the DML statements, while database administrators 
        (DBAs) use the DDL statements.



